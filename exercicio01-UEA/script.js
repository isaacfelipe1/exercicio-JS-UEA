//Faça um programa que entre com as notas
// AP1, AP2 e PF, tire a Média Final e mostre o resultado
var ap1=Number(prompt("Informe a nota da AP1"))
var ap2=Number(prompt("Informe a nota da Ap2"))
var pf=Number(prompt("Informe a nota da PF"))
var media=(ap1+ap2+pf)/3
document.write(media.toFixed(2))



